#include <iostream> // iostream used for inputting and outputting
using namespace std;

// NOTE: The For_Loop tests the condition of the loop first, and then tests the body.

// Main is always the first to run
int main()
{

    // the for_loop:
    for (int x = 0; x < 5; x++){
        cout << "Hello, world!!! " << x << endl;
    }

	return 0;
}
